package pl.coderslab.koksownik.service;

import lombok.RequiredArgsConstructor;
import org.springframework.stereotype.Service;
import pl.coderslab.koksownik.dao.WorkoutModelDao;
import pl.coderslab.koksownik.model.WorkoutModel;

import java.util.List;


@Service
@RequiredArgsConstructor
public class WorkoutModelService {
    private final WorkoutModelDao workoutModelDao;



    public List<WorkoutModel> getWorkoutTemplateById(Long id) {
        return workoutModelDao.getWorkoutTemplateById(id);
    }


}
